#ifndef WP_PWM_H
#define WP_PWM_H


#include "stm32f10x.h"


/************************************************************************************************************
**                            	宏定义相关
*************************************************************************************************************/
// PWM数量宏定义
#define WP_PWM_NUM		4	                            // PWM通道数目0~3

// 输出宏定义
#define WP_PWM0_H		(GPIOB->BSRR = GPIO_Pin_6)      // 高电平设置
#define WP_PWM0_L		(GPIOB->BRR = GPIO_Pin_6)       // 低电平设置
#define WP_PWM1_H		(GPIOB->BSRR = GPIO_Pin_7)
#define WP_PWM1_L		(GPIOB->BRR = GPIO_Pin_7)
#define WP_PWM2_H		(GPIOB->BSRR = GPIO_Pin_8)
#define WP_PWM2_L		(GPIOB->BRR = GPIO_Pin_8)
#define WP_PWM3_H		(GPIOB->BSRR = GPIO_Pin_9)
#define WP_PWM3_L		(GPIOB->BRR = GPIO_Pin_9)


/************************************************************************************************************
**                            	函数外部声明
*************************************************************************************************************/
extern void Wp_PwmConfigure(void);								    // PWM相关初始化

extern void Wp_PwmEnable(u8 Channel, FunctionalState NewState);	    // 使能PWM模式输出，通道0~3，状态ENABLE或DISABLE
extern void Wp_PwmSetDutyTime(u8 Channel, u16 DutyTime);	        // 设置高电平时长0~4095对应0%~100%
extern void Wp_PwmSetFrequency(u8 Channel, u32 Frequency);	        // 设置频率（Hz），50~1000000

extern void Wp_PwmSetIO(u8 Channel, u8 Value);				        // 设置PWM端口IO输出电平，通道0~3，值0或1


#endif
