#ifndef WP_ADC_H
#define WP_ADC_H


#include "stm32f10x.h"
#include "Wp_Globle.h"


/************************************************************************************************************
**                             函数声明
*************************************************************************************************************/
extern void Wp_AdcConfigure(void);					    // ADC配置初始化

extern u16 Wp_GetAdcChannelSamplingValue(u8 Channel);   // 获取ADC各通道采样值
extern float Wp_GetChannelAdcVoltageValue(u8 channel);  // 获取任意一个模拟输入端口电压值
extern void Wp_GetAdcVoltageValue(void);                // 获取所有模拟输入端口电压值

extern u16 Wp_AdcGetAllIO(void);					    // 获取IO采样值，返回16路IO状态，16位无符号整形每一位对应一路
extern u8 Wp_AdcGetIO(u8 channel);					    // 获取IO采样值，输入通道1~15，返回0或1

extern float Wp_FilterPower(void);                      // 电池电压滤波

extern u16 Wp_CalculateDistanceChannel(u8 channel);     // 计算红外传感器距离值
extern u16 Wp_CalculateLuxChannel(u8 channel);          // 计算光通量Lux值


#endif
